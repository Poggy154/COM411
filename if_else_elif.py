print("I am learning to paint, which direction should i paint first; up, down, left or right?")
direction = input()

if direction == "up":
    print("I am now painting upwards!")
elif direction == "down":
    print("I am now painting downwards!")
elif direction == "left":
    print("I am now painting leftwards!")
elif direction == "right":
    print(" I am now painting rightwards!")
else:
    print(f"I am now painting in {direction}!")

print("Thank you for the assistance")
